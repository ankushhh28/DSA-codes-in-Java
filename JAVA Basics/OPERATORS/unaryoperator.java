import java.util.*;

public class unaryoperator {
    public static void main(String[] args) {
        // FOR PRE-INCREMENT
        int a = 5;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        // FOR POST-INCREMENT
        int c = 7;
        int d = c++;
        System.out.println(c);
        System.out.println(d);
    }

}
