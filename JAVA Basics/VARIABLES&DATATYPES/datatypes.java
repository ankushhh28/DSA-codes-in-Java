public class datatypes {
    public static void main(String args[]) { // INTEGER DATA-TYPES
        byte a = 127;
        System.out.println(a);
        short b = 2334;
        System.out.println(b);
        int c = 465645673;
        System.out.println(c);
        long d = 557567990;
        System.out.println(d);

        // FLOAT DATA-TYPES
        float e = 3.6687777899f; // for declaring float we need to write 'f' after value bcz by default it is
                                 // considered float as double...
        System.out.println(e);
        double f = 4.56712782373467436743;
        System.out.println(f);

        // CHAR DATA-TYPE
        char ch = 98;
        System.out.println(ch);

        // BOOLEAN DATA-TYPES
        boolean g = false;
        System.out.println(g);

    }
}